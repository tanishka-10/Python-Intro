def add(val_a_param, val_b_param):
    print(float(val_a_param) + float(val_b_param))


def subtract(val_a_param, val_b_param):
    print(float(val_a_param) - float(val_b_param))


def multiply(val_a_param, val_b_param):
    print(float(val_a_param) * float(val_b_param))


def divide(val_a_param, val_b_param):
    print(float(val_a_param) / float(val_b_param))

